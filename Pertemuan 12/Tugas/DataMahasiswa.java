    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:

        model = (DefaultTableModel) JTable1.getModel();
        double nilaiAkhir = Double.parseDouble(jTextField5.getText()) * 0.1 + Double.parseDouble(jTextField6.getText()) * 0.15 + Double.parseDouble(jTextField7.getText()) * 0.25 + Double.parseDouble(jTextField8.getText()) * 0.15 + Double.parseDouble(jTextField9.getText()) * 0.35;

        model.addRow(new Object[]{
            jTextField1.getText(),
            jTextField2.getText(),
            jTextField3.getText(),
            jTextField4.getText(),
            nilaiAkhir,});

        JOptionPane.showMessageDialog(this, "Data mahasiswa berhasil ditambahkan.");

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField8.setText("");
        jTextField9.setText("");
        jTextField10.setText(Double.toString(nilaiAkhir));
    }                                        

